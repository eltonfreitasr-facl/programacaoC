#include <stdio.h>

int main() {
    int a = 10;
    int b = 20;

/*
    > (maior que)
    < (menor que)
    >= (maior ou igual a)
    <= (menor ou igual a)
    == (igual a)
    != (diferente de)
*/


    printf("a > b: %d\n", a > b);     // a maior que b
    printf("a < b: %d\n", a < b);     // a menor que b
    printf("a == b: %d\n", a == b);   // a igual a b
    printf("a != b: %d\n", a != b);   // a diferente de b

    return 0;


}